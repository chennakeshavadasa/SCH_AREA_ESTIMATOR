#!/usr/bin/env python3
"""
ihp130_measure_devices.py
=========================
Backend measurement script for the IHP130 SG13G2 area estimator.

Uses Magic VLSI (version >= 8.3.573) in batch mode to:
  1. Instantiate parameterized device PCells via the IHP130 device generator
     (magic::gencell sg13g2::<device_name>).
  2. Measure the exact physical bounding box of each instance.
  3. Sweep device parameters (W, L, nf for MOSFETs; W, L for resistors; etc.).
  4. Perform linear / bilinear regression on the measured bounding-box dimensions.
  5. Write the fitted model coefficients to ihp130/device_db.json.

Requirements:
  - Magic VLSI >= 8.3.573 installed and on PATH (or set MAGIC_EXE env var)
  - IHP-Open-PDK installed; $PDK_ROOT must point to the PDK root
  - Python >= 3.10, plus numpy and scipy for regression

Setup:
  export PDK_ROOT=/path/to/IHP-Open-PDK
  cd scripts
  python3 ihp130_measure_devices.py                # default: full sweep
  python3 ihp130_measure_devices.py --device sg13_lv_nmos --dry-run  # preview

How the Magic backend works:
  For each device/parameter combination a small TCL script is generated that:
    a) Loads the IHP130 tech + device generators via the PDK magicrc
    b) Creates a test cell using magic::gencell with the target parameters
    c) Expands the cell, reads the bounding box, and prints it to stdout
    d) Quits Magic
  Python parses the stdout, converts lambda → micrometers, and records the result.

Measurement coordinate system:
  Magic reports bounding boxes in lambda units.  The conversion factor is read
  from the tech itself via `tech lambda` (returns numerator denominator) so
  no hard-coded scale factor is needed.
"""

