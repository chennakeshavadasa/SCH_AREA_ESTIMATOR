# ─── Magic TCL template ──────────────────────────────────────────────────────

TCL_TEMPLATE = r"""
# Auto-generated by ihp130_measure_devices.py — do not edit

# ── 1. Supress GUI and load the IHP130 environment ──────────────────────────
source {magicrc}

# Confirm tech loaded
if {{[tech name] ne "ihp-sg13g2"}} {{
    puts stderr "FATAL: IHP-SG13G2 tech did not load"
    quit -noprompt
}}

# ── 2. Utility: lambda-to-micron conversion ───────────────────────────────────
proc lambda2um {{n}} {{
    set lam [tech lambda]
    # tech lambda returns {{numerator denominator}} where
    # 1 lambda = numerator/denominator micrometers
    set num [lindex $lam 0]
    set den [lindex $lam 1]
    return [expr {{$n * 1.0 * $num / $den}}]
}}

# ── 3. Batch-mode setup: ruleset + box ───────────────────────────────────────
# 1. Initialize sg13g2::ruleset explicitly and fix namespace resolution bugs in the PDK
if {{![info exists ::sg13g2::ruleset]}} {{
    namespace eval ::sg13g2 {{
        variable ruleset [dict create \
            poly_surround    0.07 diff_surround    0.07 gate_to_diffcont 0.19 \
            gate_to_polycont 0.22 gate_extension   0.18 diff_extension   0.18 \
            contact_size     0.16 via_size         0.20 metal_surround   0.05 \
            sub_surround     0.31 diff_spacing     0.21 poly_spacing     0.18 \
            diff_poly_space  0.07 diff_gate_space  0.07 metal_spacing    0.18 \
            mmetal_spacing   0.21 res_to_cont      0.20 res_diff_spacing 0.18 \
        ]
    }}
}}

# Hack to work around PDK procs referencing `$sg13g2::ruleset` without the `::` prefix.
# Tcl resolves relative namespaces from the current namespace.
namespace eval ::sg13g2::sg13g2 {{ variable ruleset $::sg13g2::ruleset }}
namespace eval ::magic::sg13g2 {{ variable ruleset $::sg13g2::ruleset }}

# ── 4. Measure one gencell instance ──────────────────────────────────────────
proc measure_gencell {{devname cellname params_dict}} {{
    # Load/create the destination cell so gencell has an edit context.
    cellname create $cellname
    load $cellname
    # Activate the box tool and place it at origin — gencell uses this as the
    # insertion point even in batch mode.
    tool box
    box 0 0 0 0
    # {{*}} expands the flat param list into separate k/v args for gencell.
    set result [catch {{
        magic::gencell $devname $cellname {{*}}$params_dict
    }} err_msg]
    if {{$result ne 0}} {{
        puts stderr "gencell FAILED for $cellname: $err_msg"
        return
    }}
    # Reload and expand to ensure full geometry is visible for bbox
    load $cellname
    select top cell
    expand
    # Output parseable result
    puts "MEASURE: $cellname"
    box
}}

# ── 4. Measurement calls (injected below) ─────────────────────────────────────
{measurement_calls}

quit -noprompt
"""


def build_tcl(magicrc: Path, calls: list[str]) -> str:
    return TCL_TEMPLATE.format(
        magicrc=str(magicrc),
        measurement_calls="\n".join(calls),
    )


# ─── Sweep point definitions ─────────────────────────────────────────────────

def mosfet_sweep(lv: bool) -> list[dict]:
    """Return list of {W, L, nf} dicts for a MOSFET sweep."""
    if lv:
        W_vals  = [0.15, 0.5, 1.0, 2.0, 4.0, 8.0, 10.0]
        L_vals  = [0.13, 0.3, 0.5, 1.0]
        nf_vals = [1, 2, 4]
        Lmin    = 0.13
    else:   # HV
        W_vals  = [0.30, 0.5, 1.0, 2.0, 4.0, 8.0]
        L_vals  = [0.45, 1.0, 2.0]
        nf_vals = [1, 2, 4]
        Lmin    = 0.45

    pts = []
    for W, L, nf in product(W_vals, L_vals, nf_vals):
        if L < Lmin:
            continue
        pts.append({"w": W, "l": L, "nf": nf, "m": 1})
    return pts


def resistor_sweep() -> list[dict]:
    """Return list of {W, L} dicts for resistor sweep."""
    pts = []
    for W in [0.5, 1.0, 2.0]:
        for L in [2.0, 5.0, 10.0, 20.0]:
            pts.append({"w": W, "l": L, "m": 1, "nx": 1})
    return pts


def cap_sweep() -> list[dict]:
    """Return list of {W, L} dicts for MIM cap sweep."""
    pts = []
    for WL in [2.0, 5.0, 10.0, 20.0]:
        pts.append({"w": WL, "l": WL, "nx": 1, "ny": 1})
    for W, L in [(5.0, 10.0), (10.0, 20.0)]:
        pts.append({"w": W, "l": L, "nx": 1, "ny": 1})
    return pts


