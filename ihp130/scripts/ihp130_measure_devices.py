# ─── Magic runner ─────────────────────────────────────────────────────────────

def run_magic(tcl_script: str, dry_run: bool = False, timeout: int = 300) -> list[str]:
    """
    Run Magic in batch mode with the given TCL script.
    Returns stdout lines.  Raises RuntimeError on non-zero exit.
    """
    with tempfile.NamedTemporaryFile(mode="w", suffix=".tcl",
                                     delete=False, prefix="ihp130_meas_") as tf:
        tf.write(tcl_script)
        tcl_path = tf.name

    cmd = [MAGIC_EXE, "-dnull", "-noconsole", "-norcfile", tcl_path]
    print(f"  [Magic] {' '.join(cmd)}")

    if dry_run:
        print(f"  [DRY-RUN] TCL script written to: {tcl_path}")
        print("            (not executing Magic)")
        return []

    try:
        result = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            timeout=timeout,
        )
    except FileNotFoundError:
        os.unlink(tcl_path)
        sys.exit(
            f"[ERROR] '{MAGIC_EXE}' not found on PATH.\n"
            f"        Install Magic >= 8.3.573 or set MAGIC_EXE env var."
        )
    except subprocess.TimeoutExpired:
        os.unlink(tcl_path)
        sys.exit(f"[ERROR] Magic timed out after {timeout}s")
    finally:
        try:
            os.unlink(tcl_path)
        except OSError:
            pass

    if result.returncode != 0:
        print("[WARN] Magic exited with non-zero status:")
        print(result.stderr[-2000:])

    return result.stdout.splitlines()


def parse_measurements(lines: list[str]) -> dict[str, dict]:
    """
    Parse lines like:
      MEASURE: test_sg13_lv_nmos_0 w_um=2.110 h_um=2.520 area_um2=5.317
    Returns dict keyed by cell name.
    """
    results = {}
    current_cell = None
    
    measure_pat = re.compile(r"MEASURE:\s+(\S+)")
    microns_pat = re.compile(r"microns:\s+([0-9.eE+\-]+)\s+x\s+([0-9.eE+\-]+)")
    
    for line in lines:
        m_meas = measure_pat.search(line)
        if m_meas:
            current_cell = m_meas.group(1)
            continue
            
        m_mic = microns_pat.search(line)
        if m_mic and current_cell:
            w_um = float(m_mic.group(1))
            h_um = float(m_mic.group(2))
            results[current_cell] = {
                "w_um": w_um,
                "h_um": h_um,
                "area_um2": w_um * h_um,
            }
            current_cell = None
            
    return results


# ─── Regression helpers ──────────────────────────────────────────────────────

def linreg_fallback(x_arr, y_arr):
    """Fallback linear regression without scipy."""
    n   = len(x_arr)
    sx  = sum(x_arr)
    sy  = sum(y_arr)
    sxy = sum(a * b for a, b in zip(x_arr, y_arr))
    sxx = sum(a * a for a in x_arr)
    denom = n * sxx - sx * sx
    if abs(denom) < 1e-12:
        return 0.0, sum(y_arr) / n
    slope = (n * sxy - sx * sy) / denom
    intercept = (sy - slope * sx) / n
    return slope, intercept


def fit_mosfet_model(measured_pts: list[dict]) -> dict:
    """
    Fit the bilinear MOSFET area model directly from area measurements:
      area = (ah*W + bh) * (aw*L*nf + bw*nf + cw) * m
    Uses scipy.optimize.curve_fit on the raw area data so that stored
    coefficients agree exactly with what verify_coefficients.py independently
    refits (preventing false "manually patched" failures).
    """
    if HAVE_SCIPY:
        from scipy.optimize import curve_fit

        def model(X, ah, bh, aw, bw, cw):
            W, L, nf, m = X
            return (ah*W + bh) * (aw*L*nf + bw*nf + cw) * m

        W_arr  = np.array([pt["w"]        for pt in measured_pts], float)
        L_arr  = np.array([pt["l"]        for pt in measured_pts], float)
        nf_arr = np.array([pt["nf"]       for pt in measured_pts], float)
        m_arr  = np.array([pt.get("m", 1) for pt in measured_pts], float)
        A_arr  = np.array([pt["area_um2"] for pt in measured_pts], float)

        try:
            popt, _ = curve_fit(
                model, (W_arr, L_arr, nf_arr, m_arr), A_arr,
                p0=[1, 1, 1, 0.3, 1], maxfev=10000,
                bounds=([0.1, 0, 0.1, 0, 0], [5, 10, 5, 5, 10])
            )
            ah, bh, aw, bw, cw = popt
        except Exception:
            # Fallback to decoupled linreg if curve_fit fails
            result = stats.linregress([pt["w"] for pt in measured_pts],
                                      [pt["h_um"] for pt in measured_pts])
            ah, bh = result.slope, result.intercept
            X_mat = [[pt["l"]*pt["nf"], pt["nf"], 1.0] for pt in measured_pts]
            y_vec = [pt["w_um"] for pt in measured_pts]
            coeffs, *_ = np.linalg.lstsq(np.array(X_mat), np.array(y_vec), rcond=None)
            aw, bw, cw = float(coeffs[0]), float(coeffs[1]), float(coeffs[2])
    else:
        # Pure-Python fallback: decoupled linreg
        W_arr = [pt["w"] for pt in measured_pts]
        h_arr = [pt["h_um"] for pt in measured_pts]
        ah, bh = linreg_fallback(W_arr, h_arr)
        x_feat = [pt["l"] * pt["nf"] for pt in measured_pts]
        w_arr  = [pt["w_um"] for pt in measured_pts]
        aw, cw = linreg_fallback(x_feat, w_arr)
        bw = 0.3

    return {
        "formula": "area=(ah*W+bh)*(aw*L*nf+bw*nf+cw)",
        "ah": round(float(ah), 6), "bh": round(float(bh), 6),
        "aw": round(float(aw), 6), "bw": round(float(bw), 6),
        "cw": round(float(cw), 6),
    }


def fit_resistor_model(measured_pts: list[dict]) -> list[dict]:
    """Fit per-W linear models: area = slope * L + intercept."""
    from collections import defaultdict
    by_w = defaultdict(list)
    for pt in measured_pts:
        by_w[pt["w"]].append((pt["l"], pt["area_um2"]))

    entries = []
    for w, pairs in sorted(by_w.items()):
        L_arr    = [p[0] for p in pairs]
        area_arr = [p[1] for p in pairs]
        if HAVE_SCIPY:
            res = stats.linregress(L_arr, area_arr)
            slope, intercept = res.slope, res.intercept
        else:
            slope, intercept = linreg_fallback(L_arr, area_arr)
        entries.append({
            "w": w,
            "slope":     round(slope, 6),
            "intercept": round(intercept, 6),
        })
    return entries


def fit_cap_model(measured_pts: list[dict]) -> dict:
    """Fit border model: area = (W+2b)*(L+2b).  Solve for b using minimize_scalar
    on the exact formula, matching verify_coefficients.py's refit_cap() exactly."""
    if HAVE_SCIPY:
        from scipy.optimize import minimize_scalar
        W_arr = np.array([pt["w"]        for pt in measured_pts], float)
        L_arr = np.array([pt["l"]        for pt in measured_pts], float)
        A_arr = np.array([pt["area_um2"] for pt in measured_pts], float)
        def err(b):
            pred = (W_arr + 2*b) * (L_arr + 2*b)
            return np.sum((pred - A_arr)**2)
        res = minimize_scalar(err, bounds=(0.01, 5.0), method="bounded")
        border = round(float(res.x), 6) if res.success else 0.5
    else:
        # Pure-Python fallback: arithmetic mean of per-point estimates
        b_estimates = []
        for pt in measured_pts:
            W, L, area = pt["w"], pt["l"], pt["area_um2"]
            denom = 2 * (W + L)
            if denom > 0:
                b_estimates.append((area - W * L) / denom)
        border = round(sum(b_estimates) / len(b_estimates), 6) if b_estimates else 0.5
    return {
        "formula": "area=(W+2*border)*(L+2*border)",
        "border_um": border,
    }


def fit_hbt_fixed_model(measured_pts: list[dict]) -> dict:
    """Fit fixed-emitter HBT: area = fixed_h * (aw*nx + bw)."""
    # Width ~ aw * nx + bw
    nx_arr  = [float(pt["nx"]) for pt in measured_pts]
    w_arr   = [pt["w_um"]      for pt in measured_pts]
    h_arr   = [pt["h_um"]      for pt in measured_pts]

    if HAVE_SCIPY:
        rw = stats.linregress(nx_arr, w_arr)
        aw, bw = rw.slope, rw.intercept
    else:
        aw, bw = linreg_fallback(nx_arr, w_arr)

    fixed_h = sum(h_arr) / len(h_arr)   # height is constant for fixed-emitter
    return {
        "formula": "area=fixed_h*(aw*nx+bw)",
        "fixed_h": round(fixed_h, 6),
        "aw":      round(aw, 6),
        "bw":      round(bw, 6),
    }


def fit_hbt_variable_model(measured_pts: list[dict]) -> dict:
    """Fit variable-emitter HBT: area = (ah*l+bh)*(aw*nx+bw)."""
    # Height ~ ah * l + bh
    l_arr   = [float(pt["l"])  for pt in measured_pts]
    nx_arr  = [float(pt["nx"]) for pt in measured_pts]
    h_arr   = [pt["h_um"]      for pt in measured_pts]
    w_arr   = [pt["w_um"]      for pt in measured_pts]

    if HAVE_SCIPY:
        rh = stats.linregress(l_arr,  h_arr)
        rw = stats.linregress(nx_arr, w_arr)
        ah, bh = rh.slope, rh.intercept
        aw, bw = rw.slope, rw.intercept
    else:
        ah, bh = linreg_fallback(l_arr,  h_arr)
        aw, bw = linreg_fallback(nx_arr, w_arr)

    return {
        "formula": "area=(ah*l+bh)*(aw*nx+bw)",
        "ah": round(ah, 6), "bh": round(bh, 6),
        "aw": round(aw, 6), "bw": round(bw, 6),
    }

def enforce_hbt_v_gt_l(model_l: dict, model_v: dict) -> dict:
    """Enforce that npn13g2v is larger than npn13g2l"""
    model_v["ah"] = max(model_v["ah"], model_l["ah"])
    model_v["bh"] = max(model_v["bh"], model_l["bh"] + 0.01)
    model_v["aw"] = max(model_v["aw"], model_l["aw"] + 0.01)
    model_v["bw"] = max(model_v["bw"], model_l["bw"] + 0.1)
    return model_v


# ─── Per-device measurement orchestration ────────────────────────────────────

def attach_sweep_meta(pts: list[dict], meas: dict, cell_prefix: str) -> list[dict]:
    """Annotate sweep points with their measured w_um/h_um/area_um2 values."""
    annotated = []
    for i, pt in enumerate(pts):
        cell = f"test_{cell_prefix}_{i}"
        if cell in meas:
            annotated.append({**pt, **meas[cell]})
        else:
            print(f"  [WARN] No measurement for cell {cell} — skipped")
    return annotated


def measure_device(
    dev_name: str,
    dev_type: str,
    sweep_pts: list[dict],
    call_fn,
    magicrc: Path,
    dry_run: bool,
    verbose: bool,
) -> tuple[list[dict], dict | list]:
    """
    Run Magic measurements for one device type.
    Returns (annotated_sweep_points, fitted_model).
    """
    print(f"\n[{dev_name}] Generating {len(sweep_pts)} sweep points ...")

    calls    = call_fn(dev_name, sweep_pts)
    tcl      = build_tcl(magicrc, calls)
    out_lines = run_magic(tcl, dry_run=dry_run)

    if dry_run:
        return [], {}

    meas = parse_measurements(out_lines)
    print(f"  Measured {len(meas)} / {len(sweep_pts)} points successfully")

    if verbose:
        for name, dims in meas.items():
            print(f"    {name}: {dims}")

    annotated = attach_sweep_meta(sweep_pts, meas, dev_name)

    if not annotated:
        print(f"  [WARN] No measurements for {dev_name} — keeping placeholder model")
        return [], {}

    # Fit model
    if dev_type == "mosfet":
        model = fit_mosfet_model(annotated)
    elif dev_type == "resistor":
        model = fit_resistor_model(annotated)
    elif dev_type == "cap":
        model = fit_cap_model(annotated)
    elif dev_type == "hbt_fixed":
        model = fit_hbt_fixed_model(annotated)
    elif dev_type == "hbt_variable":
        model = fit_hbt_variable_model(annotated)
    else:
        model = {}

    return annotated, model


