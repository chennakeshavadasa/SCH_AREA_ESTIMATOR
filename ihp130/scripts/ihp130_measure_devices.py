def main():
    parser = argparse.ArgumentParser(
        description="Measure IHP130 device bounding boxes using Magic and "
                    "fit area models for ihp130/device_db.json.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument(
        "--device", "-d", default=None,
        choices=list(DEVICE_REGISTRY.keys()),
        help="Measure only one specific device (default: all devices).")
    parser.add_argument(
        "--db", default=str(DEFAULT_DB),
        help=f"Output path for device_db.json (default: {DEFAULT_DB}).")
    parser.add_argument(
        "--pdk-root", default=None,
        help="Path to the ihp-sg13g2 PDK root (overrides $PDK_ROOT).")
    parser.add_argument(
        "--dry-run", action="store_true",
        help="Print TCL scripts without running Magic.")
    parser.add_argument(
        "--verbose", "-v", action="store_true",
        help="Print raw measurements during sweep.")
    args = parser.parse_args()

    # Locate PDK
    if args.pdk_root:
        os.environ["PDK_ROOT"] = str(Path(args.pdk_root).parent)
        pdk_root = Path(args.pdk_root)
    else:
        pdk_root = find_pdk_root()

    magicrc = pdk_root / MAGIC_RC
    if not magicrc.exists():
        sys.exit(f"[ERROR] Magic RC file not found: {magicrc}\n"
                 f"        Check your PDK installation at: {pdk_root}")

    print(f"PDK root    : {pdk_root}")
    print(f"Magic RC    : {magicrc}")
    print(f"Output DB   : {args.db}")

    # Load existing DB
    db_path = Path(args.db)
    if db_path.exists():
        with open(db_path) as fh:
            db = json.load(fh)
        print(f"Loaded existing device_db.json ({db_path})")
    else:
        print(f"[WARN] device_db.json not found at {db_path}; will create new.")
        db = {
            "pdk": "ihp-sg13g2",
            "units": "micrometers",
            "_warning": "Regenerated by ihp130_measure_devices.py",
            "mosfets": {}, "resistors": {}, "mim_caps": {}, "hbts": {},
        }

    # Choose which devices to measure
    targets = [args.device] if args.device else list(DEVICE_REGISTRY.keys())

    for dev_name in targets:
        reg = DEVICE_REGISTRY[dev_name]
        dev_type, sweep_fn, call_fn, db_cat, db_key = reg

        sweep_pts = sweep_fn()
        annotated, model = measure_device(
            dev_name, dev_type, sweep_pts, call_fn,
            magicrc, args.dry_run, args.verbose,
        )

        if args.dry_run:
            continue

        if not annotated:
            print(f"  Skipping DB update for {dev_name} (no measurements)")
            continue

        # Build formatted sweep records
        sweep_records = []
        for pt in annotated:
            rec = {k: v for k, v in pt.items()
                   if k in ("w", "l", "nf", "m", "nx", "w_um", "h_um", "area_um2")}
            sweep_records.append(rec)

        # Update the DB entry
        if db_cat not in db:
            db[db_cat] = {}
        if db_key not in db[db_cat]:
            db[db_cat][db_key] = {}

        db[db_cat][db_key]["sweep"] = sweep_records
        db[db_cat][db_key]["model"] = model

        print(f"  [{dev_name}] Model coefficients: {model}")

    if not args.dry_run:
        # Remove the PLACEHOLDER warning if ALL entries have been measured
        all_measured = all(
            db.get(cat, {}).get(key, {}).get("sweep")
            for _, _, _, cat, key in DEVICE_REGISTRY.values()
        )
        if all_measured:
            db.pop("_warning", None)
            print("\nAll devices measured — removing PLACEHOLDER warning from DB.")

        db_path.parent.mkdir(parents=True, exist_ok=True)
        with open(db_path, "w") as fh:
            json.dump(db, fh, indent=2)
        print(f"\nDevice database written to: {db_path}")
    else:
        print("\n[DRY-RUN] No files written.")


if __name__ == "__main__":
    main()
